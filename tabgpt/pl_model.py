        self.predict_step_outputs = []
        self.lr = lr
        self.config = GPT2Config(device_map="auto", num_labels=1)
        if local_model_path is not None:
            self.model = (
                GPT2ForSequenceClassificationWithoutPositionalEncoding.from_pretrained(
                    local_model_path, device_map="auto", num_labels=1
                )
            )
